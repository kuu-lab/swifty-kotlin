    println(1.coerceAtLeast(3))
    println(5.coerceAtMost(3))
    println(1.coerceAtMost(3))
    println(5L.coerceAtLeast(3L))
    println(1L.coerceAtLeast(3L))
    println(5L.coerceAtMost(3L))
    println(1L.coerceAtMost(3L))
    println(5.0.coerceAtLeast(3.0))
    println(1.0.coerceAtLeast(3.0))
    println(5.0.coerceAtMost(3.0))
    println(1.0.coerceAtMost(3.0))
    println(5.0f.coerceAtLeast(3.0f))
    println(1.0f.coerceAtLeast(3.0f))
    println(5.0f.coerceAtMost(3.0f))
    println(1.0f.coerceAtMost(3.0f))
}
