    println(5.coerceIn(1, 10))
    println(0.coerceIn(1, 10))
    println(15.coerceIn(1, 10))
    println(5L.coerceIn(1L, 10L))
    println(0L.coerceIn(1L, 10L))
    println(15L.coerceIn(1L, 10L))
