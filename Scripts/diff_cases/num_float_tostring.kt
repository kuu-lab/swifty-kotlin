// Double/Float.toString() parity with kotlinc: decimal vs scientific notation
// thresholds (|x| >= 1e7 or < 1e-3 -> scientific), special values, negative zero.
// Note: Double.MIN_VALUE / Float.MIN_VALUE shortest-form differs from Java and is
// tracked separately (see num_float_min_value.kt / SPEC-NUM-0006).
fun main() {
    println(0.0)
    println(-0.0)
    println(1.0)
    println(-1.0)
    println(100.0)
    println(0.5)
    println(0.1)
    println(0.001)
    println(0.0001)
    println(1.0E7)
    println(9999999.0)
    println(10000000.0)
    println(0.1 + 0.2)
    println(1234.5678)
    println(123456789.0)
    println(1.0E20)
    println(1.0E-20)
    println(Double.MAX_VALUE)
    println(Double.NaN)
    println(Double.POSITIVE_INFINITY)
    println(Double.NEGATIVE_INFINITY)

    println(0.0f)
    println(-0.0f)
    println(1.0f)
    println(100.0f)
    println(0.1f)
    println(0.001f)
    println(0.0001f)
    println(1.0E7f)
    println(9999999.0f)
    println(10000000.0f)
    println(1234.5677f)
    println(123456790.0f)
    println(Float.MAX_VALUE)
    println(Float.NaN)
    println(Float.POSITIVE_INFINITY)
    println(Float.NEGATIVE_INFINITY)
}
