import kotlin.math.*
fun main() {
    println(3.7.roundToInt())
    println(3.2.roundToInt())
    println((-2.5).roundToInt())
    println(3.7.roundToLong())
    println(1.0.ulp)
    println(0.0.nextUp)
    println(1.0.nextDown)
    val f: Float = 3.7f
    println(f.roundToInt())
    println(f.roundToLong())
}
