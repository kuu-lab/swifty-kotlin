import kotlin.math.*

fun main() {
    // kotlin.math constants (STDLIB-MATH-112)
    println(PI)
    println(E)

    // Double companion constants
    println(Double.POSITIVE_INFINITY)
    println(Double.NEGATIVE_INFINITY)
    println(Double.NaN)
    println(Double.MAX_VALUE > 1.0e300)
    println(Double.MIN_VALUE > 0.0)

    // Int companion constants
    println(Int.MAX_VALUE)
    println(Int.MIN_VALUE)

    // Long companion constants
    println(Long.MAX_VALUE)
    println(Long.MIN_VALUE)
}
